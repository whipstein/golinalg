		chkxer2("Zupgtr", err)
		nt = nt + 3

		//        Zupmtr
		*srnamt = "Zupmtr"
		*errt = fmt.Errorf("!left && side != Right: side=Unrecognized: /")
		err = golapack.Zupmtr('/', Upper, NoTrans, 0, 0, a.Off(0, 0).CVector(), tau, c.Off(0, 0).UpdateRows(1), w)
