		//        Zstein
		*srnamt = "Zstein"
		// *errt = fmt.Errorf("(*iblock)[j-1] < (*iblock)[j]: iblock[j-1]=%v, iblock[j]=")
		// *errt = fmt.Errorf("(*iblock)[j-1] == (*iblock)[j] && w[j-1] < w[j]: iblock[j-1]=%v, iblock[j]=%v, w[j-1]=%v, w[j]=")
