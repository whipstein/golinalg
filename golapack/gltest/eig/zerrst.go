		chkxer2("ZhetrdHb2st", err)
		*errt = fmt.Errorf("kd < 0: kd=-1")
		err = golapack.ZhetrdHb2st('Y', 'N', Upper, 0, -1, a.Off(0, 0).UpdateRows(1), d, e, c.Off(0, 0).CVector(), 1, w, 1)
		chkxer2("ZhetrdHb2st", err)
		*errt = fmt.Errorf("ab.Rows < (kd + 1): ab.Rows=1, kd=1")
		err = golapack.ZhetrdHb2st('Y', 'N', Upper, 0, 1, a.Off(0, 0).UpdateRows(1), d, e, c.Off(0, 0).CVector(), 1, w, 1)
		chkxer2("ZhetrdHb2st", err)
		*errt = fmt.Errorf("lhous < lhmin && !lquery: lhous=0, lhmin=1, lquery=false")
		err = golapack.ZhetrdHb2st('Y', 'N', Upper, 0, 0, a.Off(0, 0).UpdateRows(1), d, e, c.Off(0, 0).CVector(), 0, w, 1)
		chkxer2("ZhetrdHb2st", err)
		*errt = fmt.Errorf("lwork < lwmin && !lquery: lwork=0, lwmin=1, lquery=false")
		err = golapack.ZhetrdHb2st('Y', 'N', Upper, 0, 0, a.Off(0, 0).UpdateRows(1), d, e, c.Off(0, 0).CVector(), 1, w, 0)
		chkxer2("ZhetrdHb2st", err)
		nt = nt + 9

		//        Zungtr
		*srnamt = "Zungtr"
		*errt = fmt.Errorf("!upper && uplo != Lower: uplo=Unrecognized: /")
		err = golapack.Zungtr('/', 0, a.Off(0, 0).UpdateRows(1), tau, w, 1)
