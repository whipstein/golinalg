		*errt = fmt.Errorf("!left && side != Right: side=Unrecognized: /")
		err = golapack.Zunmtr('/', Upper, NoTrans, 0, 0, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("!upper && uplo != Lower: uplo=Unrecognized: /")
		err = golapack.Zunmtr(Left, '/', NoTrans, 0, 0, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("trans != NoTrans && trans != ConjTrans: trans=Unrecognized: /")
		err = golapack.Zunmtr(Left, Upper, '/', 0, 0, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("m < 0: m=-1")
		err = golapack.Zunmtr(Left, Upper, NoTrans, -1, 0, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("n < 0: n=-1")
		err = golapack.Zunmtr(Left, Upper, NoTrans, 0, -1, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("a.Rows < max(1, nq): a.Rows=1, nq=2")
		err = golapack.Zunmtr(Left, Upper, NoTrans, 2, 0, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(2), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("a.Rows < max(1, nq): a.Rows=1, nq=2")
		err = golapack.Zunmtr(Right, Upper, NoTrans, 0, 2, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("c.Rows < max(1, m): c.Rows=1, m=2")
		err = golapack.Zunmtr(Left, Upper, NoTrans, 2, 0, a.Off(0, 0).UpdateRows(2), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("lwork < max(1, nw) && !lquery: lwork=1, nw=2, lquery=false")
		err = golapack.Zunmtr(Left, Upper, NoTrans, 0, 2, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(1), w, 1)
		chkxer2("Zunmtr", err)
		*errt = fmt.Errorf("lwork < max(1, nw) && !lquery: lwork=1, nw=2, lquery=false")
		err = golapack.Zunmtr(Right, Upper, NoTrans, 2, 0, a.Off(0, 0).UpdateRows(1), tau, c.Off(0, 0).UpdateRows(2), w, 1)
		chkxer2("Zunmtr", err)
		nt = nt + 10

		//        Zhptrd
		*srnamt = "Zhptrd"
		*errt = fmt.Errorf("!upper && uplo != Lower: uplo=Unrecognized: /")
		err = golapack.Zhptrd('/', 0, a.Off(0, 0).CVector(), d, e, tau)
		chkxer2("Zhptrd", err)
		*errt = fmt.Errorf("n < 0: n=-1")
		err = golapack.Zhptrd(Upper, -1, a.Off(0, 0).CVector(), d, e, tau)
		chkxer2("Zhptrd", err)
		nt = nt + 2

		//        Zupgtr
		*srnamt = "Zupgtr"
		*errt = fmt.Errorf("!upper && uplo != Lower: uplo=Unrecognized: /")
		err = golapack.Zupgtr('/', 0, a.Off(0, 0).CVector(), tau, z.Off(0, 0).UpdateRows(1), w)
		chkxer2("Zupgtr", err)
		*errt = fmt.Errorf("n < 0: n=-1")
		err = golapack.Zupgtr(Upper, -1, a.Off(0, 0).CVector(), tau, z.Off(0, 0).UpdateRows(1), w)
		chkxer2("Zupgtr", err)
		*errt = fmt.Errorf("q.Rows < max(1, n): q.Rows=1, n=2")
