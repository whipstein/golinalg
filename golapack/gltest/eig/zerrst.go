		//        Zpteqr
		*srnamt = "Zpteqr"
		*errt = fmt.Errorf("icompz < 0: compz='/'")
		_, err = golapack.Zpteqr('/', 0, d, e, z.Off(0, 0).UpdateRows(1), rw)
